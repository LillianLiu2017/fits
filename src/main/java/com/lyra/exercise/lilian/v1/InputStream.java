package com.lyra.exercise.lilian.v1;

public class InputStream {
	
	private String cmd1;
	
	private String cmd2;

	public InputStream(String cmd1, String cmd2) {
		super();
		this.cmd1 = cmd1;
		this.cmd2 = cmd2;
	}

	public String getCmd1() {
		return cmd1;
	}

	public void setCmd1(String cmd1) {
		this.cmd1 = cmd1;
	}

	public String getCmd2() {
		return cmd2;
	}

	public void setCmd2(String cmd2) {
		this.cmd2 = cmd2;
	}
	
	

}
